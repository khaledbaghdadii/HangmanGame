const correctLetters = [];
const wrongLetters = [];

//Show hidden word
function displayWord() {
  wordEl.innerHTML = `${selectedWord
    .split("")
    .map(
      (letter) =>
        `<span class="letter">
      ${correctLetters.includes(letter) ? letter : ""}</span>`
    )
    .join("")}`;

  const innerWord = wordEl.innerText.replace(/\n/g, ""); //Replace new line with nothing
  console.log(innerWord);
  if (innerWord === selectedWord) {
    finalMessage.innerText = "Congratulations! You WON ðŸ˜„";
    popup.style.display = "flex";
  }
}

//Update the wrong letters
function updateWrongLettersEl() {
  //Display wrong letters
  wrongLettersEl.innerHTML = `${
    wrongLetters.length > 0 ? "<p>Wrong</p>" : ""
  } ${wrongLetters.map((letter) => `<span>${letter}</span>`)}`;

  //Display figure parts
  figureParts.forEach((part, index) => {
    const errors = wrongLetters.length;
    if (index < errors) {
      part.style.display = "block";
    } else {
      part.style.display = "none";
    }
  });

  //Check if lost
  if (wrongLetters.length == figureParts.length) {
    finalMessage.innerText = `Unfortunately! You LOST  ðŸ˜ž The word was ${selectedWord}`;
    popup.style.display = "flex";
  }
}

//Show Notification

function showNotification() {
  notifocation.classList.add("show");
  setTimeout(() => {
    notifocation.classList.remove("show");
  }, 2000);
}
//Keydown Letter press

window.addEventListener("keydown", (e) => {
  if (e.keyCode >= 65 && e.keyCode <= 90) {
    const letter = e.key;
    if (selectedWord.includes(letter)) {
      if (!correctLetters.includes(letter)) {
        correctLetters.push(letter);
        displayWord();
      } else {
        showNotification();
      }
    } else {
      if (!wrongLetters.includes(letter)) {
        wrongLetters.push(letter);
        updateWrongLettersEl();
      } else {
        showNotification();
      }
    }
  }
});

//Restart game

playAgainBtn.addEventListener("click", () => {
  //Empty the arrays
  correctLetters.splice(0);
  wrongLetters.splice(0);
  selectedWord = words[Math.floor(Math.random() * words.length)];
  displayWord();
  updateWrongLettersEl();
  popup.style.display = "none";
  console.log(selectedWord);
});
